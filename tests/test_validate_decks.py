class TestLevel3:
    def test_classify_path(self):
        a = [".inc '/path/a/model.spi'"]
        b = [".inc '/path/b/model.spi'"]
        assert _classify_hunk(a, b) == 'path'

    def test_classify_numeric(self):
        a = [".param vdd=0.450"]
        b = [".param vdd=0.900"]
        assert _classify_hunk(a, b) == 'value_numeric'

    def test_classify_extra(self):
        assert _classify_hunk([], [".extra line"]) == 'extra'

    def test_classify_missing(self):
        assert _classify_hunk([".missing line"], []) == 'missing'

    def test_classify_comment(self):
        a = ["* comment A"]
        b = ["* comment B"]
        assert _classify_hunk(a, b) == 'comment'

    def test_classify_other(self):
        a = [".tran 1p 10n"]
        b = [".dc sweep 0 1"]
        assert _classify_hunk(a, b) == 'other'

    def test_level3_diff_detected(self, tmp_path):
        """Files with real content differences -> Level 3."""
        arc_id = 'hold_CELL2_Q_rise_CP_rise_NO_CONDITION_1_1'
        dg_dir = tmp_path / 'dg' / 'hold' / arc_id
        mq_dir = tmp_path / 'mq' / 'hold' / arc_id
        make_file(str(dg_dir / 'nominal_sim.sp'), NOMINAL_A)
        make_file(str(mq_dir / 'nominal_sim.sp'), NOMINAL_DIFFERENT)

        report = validate(
            deckgen_root=str(tmp_path / 'dg'),
            mcqc_root=str(tmp_path / 'mq'),
            filename='nominal_sim.sp',
        )
        at = report['arc_types']['hold']
        assert at['level3_only_diffs'] == 1
        assert at['level1_identical'] == 0
        assert at['level2_identical'] == 0
        assert report['summary']['different'] == 1


