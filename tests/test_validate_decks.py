class TestPairing:
    def test_orphan_deckgen_only(self, tmp_path):
        """Arc in deckgen but not mcqc -> orphan_deckgen."""
        arc_id = 'hold_ORPHAN_Q_rise_CP_rise_NO_CONDITION_1_1'
        dg_dir = tmp_path / 'dg' / 'hold' / arc_id
        make_file(str(dg_dir / 'nominal_sim.sp'), NOMINAL_A)

        report = validate(
            deckgen_root=str(tmp_path / 'dg'),
            mcqc_root=str(tmp_path / 'mq'),
            filename='nominal_sim.sp',
        )
        at = report['arc_types']['hold']
        assert arc_id in at['orphans_deckgen']
        assert at['total_pairs'] == 0

    def test_orphan_mcqc_only(self, tmp_path):
        """Arc in mcqc but not deckgen -> orphan_mcqc."""
        arc_id = 'hold_ORPHAN2_Q_rise_CP_rise_NO_CONDITION_1_1'
        mq_dir = tmp_path / 'mq' / 'hold' / arc_id
        make_file(str(mq_dir / 'nominal_sim.sp'), NOMINAL_A)

        report = validate(
            deckgen_root=str(tmp_path / 'dg'),
            mcqc_root=str(tmp_path / 'mq'),
            filename='nominal_sim.sp',
        )
        at = report['arc_types']['hold']
        assert arc_id in at['orphans_mcqc']

    def test_mixed_pairs_and_orphans(self, tmp_path):
        """2 matched, 1 orphan each side."""
        for i in range(2):
            arc_id = f'hold_CELL{i}_Q_rise_CP_rise_NO_CONDITION_1_1'
            for side in ('dg', 'mq'):
                d = tmp_path / side / 'hold' / arc_id
                make_file(str(d / 'nominal_sim.sp'), NOMINAL_A)

        dg_only = tmp_path / 'dg' / 'hold' / 'hold_DGONLY_Q_rise_CP_rise_NO_CONDITION_1_1'
        mq_only = tmp_path / 'mq' / 'hold' / 'hold_MQONLY_Q_rise_CP_rise_NO_CONDITION_1_1'
        make_file(str(dg_only / 'nominal_sim.sp'), NOMINAL_A)
        make_file(str(mq_only / 'nominal_sim.sp'), NOMINAL_A)

        report = validate(
            deckgen_root=str(tmp_path / 'dg'),
            mcqc_root=str(tmp_path / 'mq'),
            filename='nominal_sim.sp',
        )
        at = report['arc_types']['hold']
        assert at['total_pairs'] == 2
        assert at['level1_identical'] == 2
        assert len(at['orphans_deckgen']) >= 1
        assert len(at['orphans_mcqc']) >= 1


