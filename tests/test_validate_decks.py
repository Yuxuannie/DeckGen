# Level 2: noise normalization
# ---------------------------------------------------------------------------

class TestLevel2:
    def test_normalize_paths(self):
        lines = [".inc '/abs/path/model.spi'\n"]
        norm = _normalize_lines(lines)
        assert norm == [".inc 'model.spi'"]

    def test_normalize_seed(self):
        lines = [".param seed=99999\n"]
        norm = _normalize_lines(lines)
        assert norm == [".param seed=1"]

    def test_normalize_monte(self):
        lines = [".param monte=500\n"]
        norm = _normalize_lines(lines)
        assert norm == [".param monte=1"]

    def test_normalize_sweep(self):
        lines = ["sweep monte 200\n"]
        norm = _normalize_lines(lines)
        assert norm == ["sweep monte 1"]

    def test_normalize_strips_comment(self):
        lines = ["* Generated by deckgen\n", ".param vdd=0.45\n"]
        norm = _normalize_lines(lines)
        assert ".param vdd=0.45" in norm
        assert not any("Generated" in l for l in norm)

    def test_normalize_strips_trailing_blank(self):
        lines = ["a\n", "\n", "\n"]
        norm = _normalize_lines(lines)
        assert norm == ["a"]

    def test_level2_identical(self, tmp_path):
        """Files differing only in abs paths / seeds are Level 2 identical."""
        arc_id = 'delay_CELL_Z_rise_A1_fall_NO_CONDITION_1_1'
        dg_dir = tmp_path / 'dg' / 'delay' / arc_id
        mq_dir = tmp_path / 'mq' / 'delay' / arc_id
        make_file(str(dg_dir / 'nominal_sim.sp'), NOMINAL_A)
        make_file(str(mq_dir / 'nominal_sim.sp'), NOMINAL_B)

        report = validate(
            deckgen_root=str(tmp_path / 'dg'),
            mcqc_root=str(tmp_path / 'mq'),
            filename='nominal_sim.sp',
        )
        at = report['arc_types']['delay']
        assert at['level1_identical'] == 0
        assert at['level2_identical'] == 1
        assert at['level3_only_diffs'] == 0


