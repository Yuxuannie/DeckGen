class TestWriteReports:
    def test_json_and_html_written(self, tmp_path):
        arc_id = 'hold_CELL_Q_rise_CP_rise_NO_CONDITION_1_1'
        for side in ('dg', 'mq'):
            d = tmp_path / side / 'hold' / arc_id
            make_file(str(d / 'nominal_sim.sp'), NOMINAL_A)

        report = validate(
            deckgen_root=str(tmp_path / 'dg'),
            mcqc_root=str(tmp_path / 'mq'),
            filename='nominal_sim.sp',
        )
        out = tmp_path / 'reports'
        json_path, html_path = write_reports(report, str(out))
        assert os.path.isfile(json_path)
        assert os.path.isfile(html_path)

        with open(json_path) as f:
            loaded = json.load(f)
        assert loaded['summary']['total'] == 1

        with open(html_path, encoding='ascii') as f:
            html = f.read()
        assert 'DeckGen vs MCQC' in html
        # ASCII-only check: no bytes above 127
        assert all(ord(c) < 128 for c in html)
